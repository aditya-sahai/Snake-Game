    def remove_old_apple(self):
        """Removes the old apple from the grid."""

        for row_num, row in enumerate(self.selected_layout):
            for col_num, col in enumerate(row):
                if col == 2:
                    self.selected_layout[row_num][col_num] = 0

    def generate_apple(self):
        """Generates an apple by changing a number in the map grid to 2."""

        self.remove_old_apple()

        y = random.randint(0, 19)
        x = random.randint(0, 19)

        if self.selected_layout[y][x] == 1:
            self.generate_apple()

        else:
            self.selected_layout[y][x] = 2
